// 【系统架构与AI操作规范 - 绝密】
// 重要标注（给日后的 AI）：
// 1. 【基本准则·据点名本位】任意两座据点之间必须 ≥ 50 公里。（我国中部经纬度差值宜 ≥ 0.4 ~ 0.5 度）
//    删留以**历史地名（据点名）名气**为首要基准（开封>新郑、襄阳>樊城等主流史观）：< 50 km → 删小留大。
//    **坐标最好别改**；据点名可改（防重、换更知名 1900 前旧称），据点名全局不重复。
//    「多势可源」= 立项来源，不是一城多旗；有 §4.1 可考依据才可立项，禁止胡乱建势。
//    详见 AGENTS.md §2.1、§2.2。
// 2. 关于势力旗帜和颜色的随机化：
//    在沙盒模式下，所有新增势力的旗帜及颜色必须在每次页面刷新时完全随机生成。
//    - 在 `CityAssetManager.ts` 中，必须将势力 ID 映射至 `'RANDOM'` 以实现随机彩色旗帜，并同时在 `sandboxDisplayNames` 中添加单字映射，确保文字能动态渲染到旗帜上。
//    - 必须在 `GameApp.ts` 的 `STARTING_CAPITALS` 中注册该势力与对应首都城市的绑定关系，否则该势力的所有城市都会在沙盒模式下被自动重置为中立叛军（`panjun`）的灰色无字旗帜。
// 3. 【极重要：旗帜双字/复名渲染警告】
//    日后 AI 必须注意：系统底层的文字雪碧图生成器 `DynamicFlagTextGenerator` 已经原生支持双字垂直排版（用于复姓或双字古国）。
//    对于历史上属于双字名称的势力（如 鸿庞 `hongbang`、大夏 `daxia`），在 `CityAssetManager.ts` 的 `sandboxDisplayNames` 中，
//    必须配置完整的双字名称（即 `'hongbang': '鸿庞'` 和 `'daxia': '大夏'`），绝对禁止擅自将其简化为单字（例如把鸿庞简写为“庞”、把大夏简写为“大”），否则即为严重功能缺陷！
// 4. 【真实历史地理严肃性规范 - 绝对禁止胡编大挪移】
//    本项目是一个基于真实历史地理的严肃沙盘。
//    日后 AI 在维护和更新城市/据点坐标时，绝对禁止为了强行满足间距规则而进行毫无根据的地理大挪移或胡编乱造！
//    - 据点名须为 **1900 年以前** 可考历史地名；禁止 1900 年后新定/改易的行政区名（详见 AGENTS.md §4.1）。
//    - 间距 < 50 km 时【绝对禁止】通过“胡编”或“地理位移”强行拉开距离。
//    - 唯一正确做法：**删小留大**（以据点名名气为准）——删小地名、留大地名（例：删中牟留开封；删秀巴堡留太昭）。
//    - **坐标不动**；据点名可因防重或换更知名旧称而改（西夜旗@叶城）。立项须有 §4.1 史料依据。见 AGENTS.md §2.1、§2.2。
// 5. 【玩家可见性 + 重复判定规则 — 2026-05-29 立】
//    玩家眼里只能看到 2 样东西：① 旗帜上的 1-2 字汉字（旗号）  ② 地图上的据点名。
//    factionId / factionName / cityId 等代码标识，玩家完全看不见，【绝不参与重复判定】。
//    重复判定**只看汉字字符串严格相等**：
//      - 旗号 "德川" === 旗号 "德川"        → ✅ 同一势力 (复用 factionId)
//      - 旗号 "德川" === 旗号 "德川幕府"     → ❌ 字符不等, 是不同势力
//      - 据点 "酒泉" === 据点 "酒泉城"       → ❌ 字符不等, 是不同据点 (会触发 50km 邻近检查)
//      - 据点 "北京" === 据点 "北京"         → ✅ 同一据点
//    势力编辑器必须按此判重，绝对禁止做 "包含 / 被包含" 的模糊匹配。
//    （详见根目录 AGENTS.md 第三节）
//
// 6. 据点后缀 → type 自动判定 (FactionEditor / batch-import 同步):
//    pass (关隘, 10000 兵): 关、口、塞、陉、径、隘、堡
//    medium_city (10000 兵): 府、京、都
//    small_city (5000 兵, 默认): 城、寨、卫、戍、镇、屯、站 等
