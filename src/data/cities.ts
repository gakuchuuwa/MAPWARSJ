// 核心规则：一个势力 = 一个据点 = 一支军队 = 一种颜色。每个 factionId 对应且仅对应一个城市。
// 数据类型：政权（如 qin/秦、han/汉、tang/唐）、
//           民族（如 xiongnu/匈奴、xianbei/鲜卑、qiang/羌）、
//           大家族（如 wang_d/琅琊王氏、xiao_d/兰陵萧氏、cui_qh_d/清河崔氏）
